    <p>The LGBTQ Glassdoor is a side project developed by <a href="https://oursky.com">Oursky</a>, a software development agency with offices in Hong Kong and Taiwan. The LGBTQ Glassdoor aims to help LGBTQ individuals find inclusive workplaces in Hong Kong. The listings on the website are submitted by individuals, LGBTQ-identified and allies, who feel an organization (a company, NGO, or public agency) is LGBTQ-friendly.</p>

    <p>We know that Hong Kong has a long way to go. Many people are not out in the workplace, sometimes even when a company has explicit same-sex or trans policies and benefits. At the same time, there may be companies that have no official policies, but have been supportive on individual cases when they are raised. Some organizations may have LGBTQ individuals and allies who want to be visible. The LGBTQ Glassdoor is meant to help capture these possibilities for LGBTQ-identified individuals to find positions in better work environments.</p>

    <p>As the listings are submitted by individuals and teams can change over time, not all of the information may be updated. We recommend that individuals find out more information through a company's website and talking with staff. The LGBTQ Glassdoor as a directory is meant to be a starting point for individuals to find information and for listed organizations to work towards greater inclusion.</p>

    <p>The LGBTQ Glassdoor is an opensource directory created with Oursky's opensource backend, <a href="https://skygear.io/">Skygear</a>. Anyone who would like to create a similar directory in their city or country can clone the LGBTQ Glassdoor repo. If you do, let us know!</p>

