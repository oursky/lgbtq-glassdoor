    <h4>怎樣定義為 LGBTQ 多元性別友善</h4>
    <p>在這個項目裡，「LGBTQ 多元性別友善」意指自我認同為 LGBTQ 的人士在一個工作場所裡即使非必要出櫃，仍然對於表現自己普遍感到自在。我們提及「普遍」是因為每個人對於舒適程度的定義都不一樣，而各個工作場所對於多元性別的包容接納程度也不一樣。舉例來說，某些工作場所可能提供同性權益，但缺乏跨性別友善政策。</p>

