    <h4>關於 Oursky</h4>
    <p>由創立至今（2018 年）將近十年的歲月裡， Oursky 並沒有太多明確的 LGBTQ 友善政策。然而，兩名已出櫃的員工能普遍感受到這裡是一個包容多元性別的友善工作場所，但卻沒有渠道可以與人分享這項資訊。我們開始好奇其他存在於香港的 LGBTQ 友善企業是否為人所知，而當我們發現無法搜索到任何有關的官方名冊，我們決定創建一個平台來透過群眾募集相關資訊。這個平台由 Oursky 的 LGBTQ 員工還有支持多元性別的員工共同合作打造。</p>

