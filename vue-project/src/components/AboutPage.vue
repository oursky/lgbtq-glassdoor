    <p>LGBTQ Glassdoor 旨在幫助 LGBTQ 同志在香港找尋更友善對待和接納他們的工作場所。網站上羅列的名單由個人、已確認多元性別身份的同志和同盟舉薦而組成，表明在該機構（公司、非牟利團體或政府機構）感受到性別友善對待。</p>

