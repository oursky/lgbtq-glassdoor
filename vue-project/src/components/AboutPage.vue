    <h4>About Oursky</h4>
    <p>By 2018, nearly a decade after its founding, Oursky didn't have many official policies. Still, two out colleagues generally felt it was an inclusive workplace, but there was nowhere to share this information. We then wondered what other LGBTQ-friendly SMEs in Hong Kong were also under the radar. When we couldn't find any official list, we decided to create a platform to crowdsource information. The directory is created through the collaboration of both Oursky's LGBTQ-identified individuals and allies.</p>

    <p>The Oursky team members who started this project in 2018 are Athena Lam, Kai Lam, Ten Tang, and May Yeung with invaluable contributions from Patrick Cheung, Jane Chung, Grace Lee, David Ng, and Queenie So.</p>

    <h4>What counts as LGBTQ-friendly?</h4>
    <p>LGBTQ-friendly for this project means that an LGBTQ-identified person generally feels comfortable to be themselves in the workplace, even if they are not necessarily out. We say “generally” because everyone has different comfort levels and also because workplaces can vary in their inclusiveness. For example, some places may have same-sex benefits, but lack trans-friendly policies.</p>

    <p>In addition, we wanted LGBTQ individuals to find relevant information for their needs. For example, trans colleagues may want to find offices with gender-neutral washrooms while a same-sex couple may want to find a company with same-sex partner benefits.</p>

