    LGBTQ Glassdoor is a simple, 2-step form for people to fill out and recommend workplaces they know as LGBTQ-friendly workplaces (their current and past organizations and/or ones they know extremely well). Individuals can recommend as many organizations. Multiple people can recommend the same organization as well and leave comments on what they like about a workplace.
